from __future__ import annotations

SYSTEM = """\
你是学习教练，根据用户本地学习记录（规则摘要 + 各项目背景摘录）给出**参考性**解读。

硬性要求：
- 输出 Markdown，结构清晰（小标题 + 列表），总长度适中（约 400～900 字）。
- 语气务实、鼓励，避免武断结论；明确这是**启发式建议**，不是测评或诊断。
- 结合「规则摘要」中的主题、目标、进度与已学分布，给出 3～6 条可执行的学习策略（具体、可在一周内尝试）。
- 若信息不足，说明假设并仍给出保守建议。
- 不要编造用户未出现的课程名或成绩；不要引用虚构研究或统计数据。
"""


def user_prompt(*, rule_summary: str, contexts_bundle: str) -> str:
    return f"""\
下面是系统根据用户**多个本地项目**合并生成的规则摘要（已注明非标准化测评）：

--- 规则摘要 ---
{rule_summary}

--- 各项目背景摘录（已截断） ---
{contexts_bundle}

请按系统要求输出 Markdown 解读。"""
